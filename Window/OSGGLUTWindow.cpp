// activate the window: bind the OGL context	
void OSGGLUTWindow::activate( void )
{
	if ( glutGetWindow() != _winid )
		glutSetWindow( _winid );
}
	
// swap front and back buffers	
void OSGGLUTWindow::swap( void )
{
	glutSwapBuffers();
}


