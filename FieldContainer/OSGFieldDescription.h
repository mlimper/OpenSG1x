struct OSGFieldDescriptionPLT
{
    bool operator()(const OSGFieldDescription* fieldDesc1, 
                    const OSGFieldDescription* fieldDesc2) const
    {
        return (fieldDesc1->getFieldId() < fieldDesc2->getFieldId());
    }
};

