/*------------- constructors & destructors --------------------------------*/

void OSGMemoryObject::addRef(void)
{
    _refCount++;
}

void OSGMemoryObject::subRef(void)
{
    _refCount--;

    if(_refCount <= 0)
        delete this;
}


